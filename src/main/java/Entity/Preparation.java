package Entity;

public class Preparation {
	 String IDUser;
	 String IDCommande;
	public Preparation() {
		super();
	}
	public Preparation(String iDUser, String iDCommande) {
		super();
		IDUser = iDUser;
		IDCommande = iDCommande;
	}
	public String getIDUser() {
		return IDUser;
	}
	public void setIDUser(String iDUser) {
		IDUser = iDUser;
	}
	public String getIDCommande() {
		return IDCommande;
	}
	public void setIDCommande(String iDCommande) {
		IDCommande = iDCommande;
	}
	 
	 

}
